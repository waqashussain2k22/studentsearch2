var tableData = document.getElementById("tabledata");
var dataSelect = document.getElementById("data");
var Districtdata = document.getElementById("Districtdata");

var uniqueDivisions = new Set();
var uniqueDistricts = new Set();

for (var i = 0; i < data.length; i++) {
    uniqueDivisions.add(data[i].Division);
}

uniqueDivisions.forEach(division => {
    dataSelect.innerHTML += `<option value="${division}">${division}</option>`;
});

dataSelect.addEventListener("change", function () {
    tableData.innerHTML = "";

    var selectedDivision = dataSelect.value;

    Districtdata.innerHTML = `<option value="" selected disabled>Select District</option>`;


    uniqueDistricts.clear();
    for (var k = 0; k < data.length; k++) {
        if (selectedDivision === "" || data[k].Division === selectedDivision) {
            uniqueDistricts.add(data[k].District);
        }
    }


    uniqueDistricts.forEach(district => {
        Districtdata.innerHTML += `<option value="${district}">${district}</option>`;
    });


    updateTable();
});


Districtdata.addEventListener("change", updateTable);

